<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">绑定账号</h3>
      </div>
      <input type="hidden" v-model="loginForm.openId"/>
      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input ref="username" v-model="loginForm.username" placeholder="Username" name="username" />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input   :type="passwordType" ref="password" v-model="loginForm.password" placeholder="Password" name="password"/>
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="bindWeChatUser">绑定</el-button>

    </el-form>
  </div>
</template>

<script>

import { setToken } from '../../utils/auth'

export default {
  data() {
    return {
      loginForm: {
        openId :'',
        username: '',
        password: ''
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur' }],
        password: [{ required: true, trigger: 'blur' }]
      },
      passwordType: 'password',
    }
  },
  mounted(){
    let request = location.href
    let strings = request.split("?")
    let param = strings[1].split("=")
    this.loginForm.openId = param[1]
    console.log(this.loginForm,param)
  },
  created() {
    this.$Notice.config({
      top: 50,
      duration: 3
    });
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    bindWeChatUser(){
      this.$http.post("/wechat/bind",this.loginForm).then(res=>{
        //登录成功
        // sessionStorage.setItem('user', JSON.stringify(res.data.data.user));
        //
        // sessionStorage.setItem('sessionId', res.data.data.sessionId);
        if (res.data.success){
          setToken(res.data.data.sessionId)
          this.$router.push("/dashboard")
        }else {
          this.$Message.error(res.data.message);
        }
      }).catch(error => {
        console.log(error);
        this.$Notice.error({
          title: '操作通知',
          desc: '操作失败' +error.message
        });
      })
    }
  }
}
</script>

